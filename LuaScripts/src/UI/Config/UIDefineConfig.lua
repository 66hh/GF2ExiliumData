function UIDefineConfig.GetUIDefine(uiType)
  local uiDef = UIDefineConfig[uiType]
  if uiDef ~= nil then
    return uiDef.UIPrefab, uiDef.UIName, uiDef.LuaPath
  end
  return nil
end
function UIDefineConfig.GetUIType(uiName)
  for uiType, uiDef in pairs(UIDefineConfig) do
    if uiDef ~= nil and type(uiDef) == "table" and uiDef.UIName == uiName then
      return uiType
    end
  end
  return nil
end
